def printingDice(n):
    print("Dice has been rolled")
    print("[-------------]")
    if (n == 1):
        print("[             ]")
        print("[      *      ]")
        print("[             ]")
    if (n == 2):
        print("[             ]")
        print("[   *     *   ]")
        print("[             ]")
    if (n == 3):
        print("[      *      ]")
        print("[      *      ]")
        print("[      *      ]")
    if (n == 4):
        print("[  *       *  ]")
        print("[             ]")
        print("[  *       *  ]")
    if (n == 5):
        print("[  *       *  ]")
        print("[      *      ]")
        print("[  *       *  ]")
    if (n == 6):
        print("[  *       *  ]")
        print("[  *       *  ]")
        print("[  *       *  ]")    
    print("[-------------]")
